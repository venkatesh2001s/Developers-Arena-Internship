#Create variables to store different types of data

# String: Used for text
name = "Venkatesh"
print(f"Name   : {name}")

# Integer: Used for whole numbers
age = 22
print(f"Age   : {age}")

# Float: Used for decimal numbers
height = 5.8
print(f"Height   : {height}")

# Boolean: Used for True/False values
is_student = True
print(f"Student   : {is_student}")

# List: Used for an ordered sequence of items
hobbies = ["coding", "music", "chess"]
print(f"Hobbies   : {hobbies}")

# Tuple: Used for an ordered, immutable sequence
coordinates = (17.4, 78.5)
print(f"Coordinates   : {coordinates}")

# Dictionary: Used for key-value pairs
personal_info = {"city": "Hyderabad", "country": "India"}
print(f"Personal_information   : {personal_info}")

# Set: Used for unique items (unordered)
skills = {"Python", "SQL", "Java"}
print(f"Technical_skills   : {skills}")
